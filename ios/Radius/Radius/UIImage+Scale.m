- (UIImage*)scaleToSize:(CGSize)maxSize
{
    
    CGSize newSize;
    
    if((self.size.height <= maxSize.height) && (self.size.width <= maxSize.width)) {
        // already smaller than maxSize box
        newSize = self.size;
    } else {
        if(self.size.height/maxSize.height > self.size.width/maxSize.width) {
            // shrink so whole height fits
            newSize.height = maxSize.height;
            newSize.width = self.size.width * maxSize.height/self.size.height;
        } else {
            // shrink so whole width fits
            newSize.width = maxSize.width;
            newSize.height = self.size.height * maxSize.width/self.size.width;
        }
    }
    
    NSLog(@"old: %fx%f",self.size.width,self.size.height);
    NSLog(@"new: %fx%f",newSize.width,newSize.height);
    
    CGSize roundedSize = CGSizeMake(floor(newSize.width), floor(newSize.height));
    
    NSLog(@"rounded: %fx%f",roundedSize.width,roundedSize.height);
    
    return [self resizedImageWithContentMode:UIViewContentModeScaleAspectFill bounds:roundedSize interpolationQuality:kCGInterpolationHigh];
    
