- (UIImage*)scaleToWidth:(CGFloat)width
{
    CGFloat heightRatio = self.size.height/self.size.width;
    CGFloat newHeight = heightRatio * width;
    CGSize newSize = CGSizeMake(width, newHeight);
    
    UIGraphicsBeginImageContext(newSize);
    [self drawInRect:CGRectMake(0,0,newSize.width,newSize.height)];
    UIImage* newImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return newImage;
}

- (UIImage*)scaleToHeight:(CGFloat)height
{
    CGFloat widthRatio = self.size.height/self.size.width;
    CGFloat newWidth = widthRatio * height;
    CGSize newSize = CGSizeMake(newWidth, height);
    
    UIGraphicsBeginImageContext(newSize);
    [self drawInRect:CGRectMake(0,0,newSize.width,newSize.height)];
    UIImage* newImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return newImage;
}


